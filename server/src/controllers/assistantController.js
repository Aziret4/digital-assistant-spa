const FALLBACK_ANSWER =
  'Здравствуйте! Спасибо за ваше обращение. ' +
  'Чтобы подготовить точный ответ, подскажите, пожалуйста, детали: что именно нужно сделать и в какие сроки. ' +
  'Я свяжусь с вами в ближайшее время.';

function generateAnswer(question) {
  const lower = question.toLowerCase();
  for (const tpl of TEMPLATES) {
    if (tpl.keywords.some((kw) => lower.includes(kw))) {
      return tpl.answer;
    }
  }
  return FALLBACK_ANSWER;
}

async function ask(req, res) {
  try {
    const { question } = req.body;

    if (!question || typeof question !== 'string' || question.trim().length < 2) {
      return res.status(400).json({ message: 'Поле question обязательно' });
    }

    const answer = generateAnswer(question);
    res.json({ question, answer });
  } catch (err) {
    console.error('assistant ask error:', err);
    res.status(500).json({ message: 'Ошибка цифрового помощника' });
  }
}

module.exports = { ask };
